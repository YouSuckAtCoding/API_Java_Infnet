package infnet.edu.tp2.Model;

public class Product {
    private int Id;

    public int getid() {
        return Id;
    }

    public void setid(int id) {
        Id = id;
    }

    private String Name;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    private float Price;

    public float getPrice() {
        return Price;
    }

    public void setPrice(float price) {
        Price = price;
    }

}
